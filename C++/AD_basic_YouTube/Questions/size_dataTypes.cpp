#include<iostream>
using namespace std;
int main()
{
    int a;
    cout << "Size of Integer: "<< sizeof(a)<< endl;

    float b;
    cout << "Size of Float: "<< sizeof(b)<< endl;

    char c;
    cout << "Size of Character: "<< sizeof(c)<< endl;

    bool d;
    cout << "Size of Boolean: "<< sizeof(d)<< endl;

    double e;
    cout << "Size of Double: "<< sizeof(e)<< endl;

    short int f;
    cout << "Size of Double: "<< sizeof(f)<< endl;

    long int g;
    cout << "Size of Double: "<< sizeof(g)<< endl;

    return 0;
}